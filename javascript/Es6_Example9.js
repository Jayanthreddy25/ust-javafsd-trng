// function expression 
 
var myfun1= function show() { 
    console.log("Itis a Function Expression"); 
    } 
     
    // Anonymousfunction 
     
    var myfun2= function () { 
     console.log("Itis an Anonymous Function"); 
     } 
     
    //Arrow function 
     
    var myfun3= () => { 
     console.log("Itis an Arrow Function"); 
     }; 
     
    myfun1(); 
    myfun2(); 
    myfun3(); 